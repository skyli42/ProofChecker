
public class Theorem {
	int Ch;
	String Name;
	String i;
	String t;
	public Theorem(int Ch,String Name,String i,String t){
		this.Ch = Ch;
		this.Name = Name;
		this.i = i;
		this.t = t;
	}
	public int getCh() {
		return Ch;
	}
	public void setCh(int ch) {
		Ch = ch;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getI() {
		return i;
	}
	public void setI(String i) {
		this.i = i;
	}
	public String getT() {
		return t;
	}
	public void setT(String t) {
		this.t = t;
	}
	
}
